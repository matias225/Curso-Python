# Programa: Ejemplo de caracteres especiales

# Salto de linea \n
mensaje1 = "Hola\nmundo"
print(mensaje1)

# Tabulación \t
# Imprimimos una guía númerica para ver las posiciones
print(123412341341234)
mensaje2 = "\t\tMatias Romani"
print(mensaje2)

# Comillas dentro de cadenas
mensaje3 = "Ella dijo: \"Hola a todos\""
print(mensaje3)

# Barra invertida \
mensaje4 = "C:\\Users\\Matias\\Documents"
print(mensaje4)

# Cadenas cruda (raw string) r
mensaje5 = r"C:\Users\Matias\Documents"
print(mensaje5)
