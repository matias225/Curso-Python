# Programa: Multiplicación de cadenas

# 1. Crear un separador visual
linea = "-" * 20
print(linea) # Salida: ----------------------

# 2. Indentación simple
nivel = 2
sangria = "    " * nivel
print(sangria + "definición bloque código") # Salida:         definición bloque código

# 3. Patrones simples
print("1" * 10) # Salida: 1111111111 (texto, no número)

