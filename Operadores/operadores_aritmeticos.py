# Operadores Aritmeticos
a = 10
b = 3

# Suma +
suma = a + b
print(f"Suma: {suma}")

# Resta -
resta = a - b
print(f"Resta: {resta}")

# Multiplicación *
multiplicar = a * b
print(f"Multiplicación: {multiplicar}")

# División /
dividir = a / b
print(f"División: {dividir:.2f}")

# División entera (//)
division_entera = a // b
print(f"División entera: {division_entera}")

# Módulo (%) residuo de la división
modulo = a % b
print(f"Residuo de la división: {modulo}")

# Exponente (potencia) **
exponente = a ** b
print(f"Exponente: {exponente}")