print("*** Operadores de Asignación ***")
numero = 5
print(f"Valor de número: {numero}")
numero = 10
print(f"Valor de número: {numero}")
cadenas = "Saludos desde Python"
print(f"Valor de cadenas: {cadenas}")

# Asignación múltiple
x, y, z = 5, "Hola", -9.15
print(f"Valor de x = {x}, y = {y}, z = {z}")

# Asignación encadenada
a = b = c = 10
print(f"Valor de a = {a}, b = {b}, c = {c}")